# Arithmetic operators

# Addition +
# Subtraction -
# Multiplication *
# Division /
# Integer Division //
# Exponentiation **
# Modulus %

a = 18
b = 5

# Addition
c = a + b
print(f"{a} + {b} = {c}")

# Subtraction
c = a - b
print(f"{a} - {b} = {c}")

# Multiplication
c = a * b
print(c)

# Division
c = a / b
print(c)

# Integer div.
c = a // b
print(c)

c = a**b
print(c)

c = a % b
print(c)
