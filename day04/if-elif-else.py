some_number = int(input("Enter a number [1-7]: "))

if some_number == 1:
    print("Monday")
elif some_number == 2:
    print("Tuesday")
elif some_number == 3:
    print("Wednesday")
elif some_number == 4:
    print("Thursday")
elif some_number == 5:
    print("Friday")
elif some_number == 6:
    print("Saturday")
elif some_number == 7:
    print("Sunday")
else:
    print("You input a wrong number")
